2 > 2 // false
2 >= 2 // true (större eller lika med)

//Mindre än: <
1 < 2 // true
2 < 1 // false
1 < 1 // false
1 <= 1 // true (mindre eller lika med)

//Lika med
1 == 1 //true
1 == "1" // true (jämför bara värde)
1 === 1 // true
1 === "1" // false (jämför både datatyp och värde)

//Skilt från
1 != 2 //true
1 !== 1 // false
1 !== "1" // false (jämför både datatyp och värde)

// aritmetiska operationer 
2 + 1 // 3
2 - 1 // 1
2 * 1 // 2
2 / 1 // 2

